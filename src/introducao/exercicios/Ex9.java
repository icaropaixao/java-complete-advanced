import  java.util.Scanner;
public class Ex9 {
    public static void main(String[] args) {

        // contagem regressiva com for
        for (int i = 3; i >=0  ; i--){
            System.out.println(i);
        }

        // contagem crescente com for
        for(int i = 0; i <=3 ; i++){
            System.out.println(i);
        }

    }
}
